class BinaryLifting:
    def __init__(self, root, graph, deapth):
        self.root = root
        n = len(graph)
        self.L = L = n.bit_length() + 1
        self.parent = [[0] * L for _ in range(n)]
        self.depth = deapth
        q = deque([root])
        vis = [False] * n
        while q:
            node = q.popleft()
            vis[node] = True
            for nei in graph[node]:
                if not vis[nei]:
                    self.parent[nei][0] = node
                    q.append(nei)
        for i in range(1, L):
            for j in range(n):
                self.parent[j][i] = self.parent[self.parent[j][i - 1]][i - 1]

    def getKthAncestor(self, node, k):
        for i in range(self.L):
            if k & (1 << i):
                node = self.parent[node][i]
        return node


from types import GeneratorType
def bootstrap(f, stack=[]):
    def wrappedfunc(*args, **kwargs):
        if stack:
            return f(*args, **kwargs)
        else:
            to = f(*args, **kwargs)
            while True:
                if type(to) is GeneratorType:
                    stack.append(to)
                    to = next(to)
                else:
                    stack.pop()
                    if not stack:
                        break
                    to = stack[-1].send(to)
            return to
    return wrappedfunc

@bootstrap
def dfsDeapth(node, par, depth, graph, deapth):
    deapth[node] = depth
    for nei in graph[node]:
        if nei != par:
            yield dfsDeapth(nei, node, depth + 1, graph, deapth)
    yield


